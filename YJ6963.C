void yj_pr1(uchar dat1,uchar dat2,uchar com)	/*双字节参数指令写入*/
	{
	while ((YJ_C_ADD & 0x03) !=0x03);	/*读写指令和读写数据状态1:准备好，0:忙*/
	YJ_D_ADD=dat1;				/*第一个参数写入数据通道*/
	while ((YJ_C_ADD & 0x03) !=0x03);
	YJ_D_ADD=dat2;				/*第二个参数写入数据通道*/
	while ((YJ_C_ADD & 0x03) !=0x03);
	YJ_C_ADD=com;				/*指令代码写入指令通道*/
	}
void yj_pr11(uchar dat2,uchar com)			/*单字节参数指令写入*/
	{
	while ((YJ_C_ADD & 0x03) !=0x03);
	YJ_D_ADD=dat2;				/*第一个参数写入数据通道*/
	while ((YJ_C_ADD & 0x03) !=0x03);
	YJ_C_ADD=com;				/*指令代码写入指令通道*/
	}
void yj_pr12(uchar com)			/*无参数指令写入*/
	{
	while ((YJ_C_ADD & 0x03) !=0x03);
	YJ_C_ADD=com;				/*指令代码写入指令通道*/
	}
